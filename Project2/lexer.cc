//
// if you want to unget all three tokens. Note that it does not
// make sense to unget t1 without first ungetting t2 and t3
//
TokenType LexicalAnalyzer::UngetToken(Token tok)
{
    tokens.push_back(tok);;
    return tok.token_type;
}

Token LexicalAnalyzer::GetToken()
{
    char c;

    // if there are tokens that were previously
    // stored due to UngetToken(), pop a token and
    // return it without reading from input
    if (!tokens.empty()) {
        tmp = tokens.back();
        tokens.pop_back();
        return tmp;
    }

    SkipSpace();
    tmp.lexeme = "";
    tmp.line_no = line_no;
    input.GetChar(c);
    switch (c) {
        case 'p':
            char ur,y;
            input.GetChar(ur);//r or u for pRivate or pUblic respectively
            if(ur == 'u')
            {
            tmp.token_type = PUBLIC;
            input.GetChar(ur);input.GetChar(ur);input.GetChar(ur);input.GetChar(ur);
            return tmp;
            }
            if(ur == 'r')
            {
            tmp.token_type = PRIVATE;
            input.GetChar(ur);input.GetChar(ur);input.GetChar(ur);input.GetChar(ur);input.GetChar(ur);
            return tmp;
            }
            input.UngetChar(ur);
        case '=':
            tmp.token_type = EQUAL;
            return tmp;
        case ':':
            tmp.token_type = COLON;
            return tmp;
        case ',':
            tmp.token_type = COMMA;
            return tmp;
        case ';':
            tmp.token_type = SEMICOLON;
            return tmp;
        case '{':
            tmp.token_type = LBRACE;
            return tmp;
        case '}':
            tmp.token_type = RBRACE;
            return tmp;
        case '/':
            char slash;
            input.GetChar(slash);
            if(slash == '/')
            {
            return SkipComment();
            }
            else{input.UngetChar(slash);}
        default:
            if (isdigit(c)) {
                input.UngetChar(c);
                //return ScanNumber();
            } else if (isalpha(c)) {
                input.UngetChar(c);
                return ScanIdOrKeyword();
            } else if (input.EndOfInput())
                tmp.token_type = END_OF_FILE;
            else
                tmp.token_type = ERROR;

            return tmp;
    }
}
